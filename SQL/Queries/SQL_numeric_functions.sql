-- ABS() - Return absolute value
SELECT ABS(66), ABS(-66);

-- CEILING() - Return the smallest integer value not less than the argument
SELECT CEILING(12.34), CEILING(-12.34);

-- COS() - Returns cosine
SELECT DEGREES(PI()), DEGREES(PI()/2);

-- FLOO() - rETURN THE LARGEST INTEGER VALUE NOT GREATER THAN THE ARGUMENT
SELECT FLOOR(12.34), FLOOR(-12.34);

-- PI() - Returns the value of PI()
SELECT PI()
SELECT PI()+0.000000000000;

-- POWER() - Return the argument raised to the specified power - synonym of POW()
SELECT POWER(2,2);
SELECT POWER(8,-2);

-- SQRT() - Return squared root of argument
SELECT SQRT(4);
SELECT SQRT(64);

